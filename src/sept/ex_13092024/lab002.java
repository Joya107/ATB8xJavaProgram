package sept.ex_13092024;

public class lab002 {
    public static void main(String[] args) {
        double a = 34;
        double b = 10;

        System.out.printf("a + b = %f%n", a + b);    // Addition
        System.out.printf("a - b = %f%n", a - b);    // Subtraction
        System.out.printf("a * b = %f%n", a * b);    // Multiplication
        System.out.printf("a / b = %f%n", a / b);    // Division
        System.out.printf("a %% b = %f%n", a % b);   // Modulus (remainder)
    }
}
